# indexing: mengambil nilai dengan  index
var = "aku keren"
print(var[0])
print(var[-5])
# slicng: mengambil potongan nilai [star:stop:step]
name = " belajar python"
print(name[0:-5])
print(name[0:8])
print(name[8:15])
print(name[2:])
print(name[:8])
print(name[:8:2])
print(name[::-1])
# concanation (bisa memnambahkan kata) dan multiplikasion(bisa menggandakan kata)
print(var+name)
print(name*2)
